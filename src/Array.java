public class Array {
    public static void main(String[] args){
        char[] SteveJob = new char[5];
        SteveJob[0] = 'A';
        SteveJob[1] = 'B';
        for (int i = 0; i < SteveJob.length; i++) {
            System.out.println("SteveJob[" + i + "] = " + SteveJob[i]);
        }
    }
}
